import Foundation
import CryptoKit

var data:[UInt8] = [0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff]

var digest:SHA256Digest? = nil

for _ in 0 ..< 0x1ffffff {
    digest = SHA256.hash(data:data)
    data = []
    digest?.withUnsafeBytes {buffer in
        data.append(contentsOf: buffer)}
}

print(digest?.description as Any)

